async def main():

    url = RAW_URL
    if url.startswith("postgresql+asyncpg://"):
        url = url.replace("postgresql+asyncpg://", "postgresql://", 1)
    needs_ssl = "?" in url
    url = url.split("?")[0]

    conn = await asyncpg.connect(url, ssl="require" if needs_ssl else False)

    row = await conn.fetchrow("SELECT id FROM users WHERE is_admin = TRUE LIMIT 1")
    if not row:
        row = await conn.fetchrow("SELECT id FROM users LIMIT 1")
    if not row:
        print("유저가 없어요. 먼저 회원가입 해주세요.")
        await conn.close()
        return

    user_id = row["id"]
    print(f"✅ user_id={user_id} 로 페르소나 생성 시작")

    inserted = 0
    for name, personality, background, speech_style, tags in PERSONAS:
        system_prompt = build_system_prompt(name, personality, background, speech_style)
        await conn.execute(
            """
            INSERT INTO personas
                (user_id, name, personality, background, speech_style, system_prompt, is_public, chat_count, tags)
            VALUES ($1,$2,$3,$4,$5,$6,TRUE,0,$7)
            ON CONFLICT DO NOTHING
            """,
            user_id, name, personality, background, speech_style, system_prompt, tags
        )
        inserted += 1
        print(f"  [{inserted}] {name}")

    await conn.close()
    print(f"\n🎉 완료! 총 {inserted}개 페르소나 삽입")

if __name__ == "__main__":
    asyncio.run(main())
